const EXTRA_NOTES: Partial<Record<NoteType, string>> = {
  chatgpt_log:
    '個人情報や非公開情報が含まれる場合は「Gitに上げる場合の注意」に明記してください。',
  jobsearch:
    '個人情報（氏名、住所、連絡先、応募先の非公開情報など）が含まれる場合は「個人情報の有無」に明記してください。',
};

export function buildChatGptPrompt(type: NoteType): string {
  const label = getNoteCategoryLabel(type);
  const lines = [
    `以下の内容を、${label}としてMarkdown形式で整理してください。`,
    '',
    '指定した見出しを使ってください。',
    '不明な項目は無理に埋めず「未記載」または「該当なし」としてください。',
    '推奨タグを最後に出してください。',
  ];
  const extra = EXTRA_NOTES[type];
  if (extra) {
    lines.push(extra);
  }
  lines.push('', '出力形式：', '', TEMPLATE_BODIES[type], '', '整理対象：');
  return lines.join('\n');
}
